//-------------------------------------------------------------------------------
// Imports
//-------------------------------------------------------------------------------

import {
    Class
} from 'bugcore';
import Entity from './Entity';


//-------------------------------------------------------------------------------
// Declare Class
//-------------------------------------------------------------------------------

/**
 * @class
 * @extends {Entity}
 */
const UserEntity = Class.extend(Entity, {

    _name: 'bitpack.UserEntity',


    //-------------------------------------------------------------------------------
    // Getters and Setters
    //-------------------------------------------------------------------------------

    /**
     * @return {string}
     */
    getEmail() {
        return this.getRawData().email;
    },

    /**
     * @return {string}
     */
    getId() {
        return this.getRawData().id;
    },

    /**
     * @return {string}
     */
    getUsername() {
        return this.getRawData().username;
    }
});


//-------------------------------------------------------------------------------
// Exports
//-------------------------------------------------------------------------------

export default UserEntity;
